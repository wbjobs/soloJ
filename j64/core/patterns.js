function placePattern(grid, patternName, x, y) {
  const pattern = patterns[patternName];
  if (!pattern) return;
  for (let dy = 0; dy < pattern.length; dy++) {
    for (let dx = 0; dx < pattern[dy].length; dx++) {
      if (pattern[dy][dx]) {
        grid.set(x + dx, y + dy, 1);
      }
    }
  }
}

